ALTER TABLE "Documentos_Donantes" ADD FOREIGN KEY ("org_id") REFERENCES "Organizaciones" ("org_id") ON DELETE CASCADE;

ALTER TABLE "Documentos_Donantes" ADD FOREIGN KEY ("donante_id") REFERENCES "Donantes" ("donante_id") ON DELETE CASCADE;

