ALTER TABLE "Donaciones" ADD FOREIGN KEY ("donante_id") REFERENCES "Donantes" ("donante_id") ON DELETE CASCADE;

ALTER TABLE "Donaciones" ADD FOREIGN KEY ("org_id") REFERENCES "Organizaciones" ("org_id") ON DELETE CASCADE;

ALTER TABLE "Donaciones" ADD FOREIGN KEY ("uma_id") REFERENCES "Configuracion_UMA" ("uma_id") ON DELETE SET NULL;

